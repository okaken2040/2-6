# 数値

puts 100
# 出力結果=>100
puts 100 + 5
# 出力結果=>105


# 文字列

puts "Rubyを学習"
# 出力結果=>Rubyを学習
name = "鈴木さん"
puts "Hello" + name
# 出力結果=>Hello鈴木さん
puts "こんにちは#{name}"
# 出力結果=>こんにちは鈴木さん



# 配列

names = ["Perl", "Ruby", "Python", "Swift"]
puts names[1]
 
# 出力結果=>Ruby



# ハッシュ

tall = {"太郎"=>185, "二郎"=>170, "花子"=>150}
puts tall["太郎"]
# 出力結果=>185


# シンボル

tall = {:太郎=>185, :二郎=>170, :花子=>150}
puts tall[:花子]
# 出力結果=>150 


